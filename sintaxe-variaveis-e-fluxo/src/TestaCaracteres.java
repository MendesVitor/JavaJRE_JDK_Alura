
public class TestaCaracteres {

	public static void main(String[] args) {
		char letra = 'v';
		System.out.println(letra);

		char valor = 66;
		System.out.println(valor);

		valor = (char) (valor + 1);
		System.out.println(valor);

		String palavra = "teste de palavra";
		System.out.println(palavra);

		palavra = palavra + 1993;
		System.out.println(palavra);
	}

}
